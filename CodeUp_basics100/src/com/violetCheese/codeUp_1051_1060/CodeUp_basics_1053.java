package com.violetCheese.codeUp_1051_1060;

import java.util.Scanner;

public class CodeUp_basics_1053 {
	
	//메인 메서드
	public static void main(String[] args) {
		
        Scanner sc = new Scanner(System.in);
        String str = sc.nextLine();
        if (str.equals("1")){
            System.out.print(0);
        }else if (str.equals("0")){
            System.out.print(1);
        }
        sc.close();
        
	}//main 메서드 끝
	
}//CodeUp_basics_1053 클래스 끝
